package com.quarkdown.processor.generation

import com.google.devtools.ksp.processing.CodeGenerator
import com.google.devtools.ksp.processing.Dependencies
import com.quarkdown.processor.model.FunctionDescriptor
import com.quarkdown.processor.model.ModuleDescriptor
import com.quarkdown.processor.model.ParameterDescriptor
import com.quarkdown.processor.util.ModuleNaming
import com.quarkdown.processor.util.backtick
import com.quarkdown.processor.util.backtickLastSegment

/**
 * Emits one Kotlin source per discovered [ModuleDescriptor]: an `object <ModuleName>`
 * containing synthetic wrappers around the source `@QFunction`s and a nested
 * `val Module: QuarkdownModule = moduleOf(::wrapper, ...)`.
 *
 * @param codeGenerator KSP [CodeGenerator] used to create the output streams
 */
class ModuleCodeGenerator(
    private val codeGenerator: CodeGenerator,
) {
    /**
     * Generates the source for [module] and writes it to KSP's output directory.
     */
    fun generate(module: ModuleDescriptor) {
        val dependencies = Dependencies(aggregating = false, module.file)
        val fileName = ModuleNaming.generatedFileNameOf(module.name)

        codeGenerator
            .createNewFile(dependencies, module.packageName, fileName, extensionName = ModuleNaming.KOTLIN_EXTENSION)
            .bufferedWriter()
            .use { writer -> writer.write(render(module)) }
    }

    private fun render(module: ModuleDescriptor): String =
        buildString {
            appendLine("// Generated by quarkdown-native-library-processor. Do not edit.")
            // Ktlint scans KSP output when the plugin is applied to a KSP-enabled source set;
            // suppressing at the file level keeps generated code out of the linter's way.
            appendLine("@file:Suppress(")
            appendLine("\t\"RedundantVisibilityModifier\",")
            appendLine("\t\"RemoveRedundantBackticks\",")
            appendLine("\t\"unused\",")
            appendLine("\t\"ktlint:standard:annotation\",")
            appendLine("\t\"ktlint:standard:argument-list-wrapping\",")
            appendLine("\t\"ktlint:standard:chain-method-continuation\",")
            appendLine("\t\"ktlint:standard:filename\",")
            appendLine("\t\"ktlint:standard:function-naming\",")
            appendLine("\t\"ktlint:standard:function-signature\",")
            appendLine("\t\"ktlint:standard:import-ordering\",")
            appendLine("\t\"ktlint:standard:indent\",")
            appendLine("\t\"ktlint:standard:max-line-length\",")
            appendLine("\t\"ktlint:standard:no-unused-imports\",")
            appendLine("\t\"ktlint:standard:parameter-list-wrapping\",")
            appendLine("\t\"ktlint:standard:parameter-wrapping\",")
            appendLine("\t\"ktlint:standard:paren-spacing\",")
            appendLine(")")
            appendLine()
            appendLine("package ${module.packageName}")
            appendLine()
            appendLine("import $QUARKDOWN_MODULE_FQN")
            appendLine("import $MODULE_OF_FQN")
            module.sourceImports?.let {
                appendLine()
                appendLine(it)
            }
            appendLine()
            appendLine("object ${module.name} {")
            appendModuleVal(module)
            module.functions.forEach { function ->
                appendLine()
                appendWrapper(function)
            }
            appendLine("}")
        }

    private fun StringBuilder.appendModuleVal(module: ModuleDescriptor) {
        appendLine("\tval Module: QuarkdownModule =")
        appendLine("\t\tmoduleOf(")
        module.functions.forEach { function ->
            appendLine("\t\t\tthis::${function.exportedName.backtick()},")
        }
        appendLine("\t\t)")
    }

    private fun StringBuilder.appendWrapper(function: FunctionDescriptor) {
        val signatureEntries = function.parameters.flatMap { it.signatureContributions() }
        val parameters = signatureEntries.joinToString(", ", transform = ::renderPlain)
        val returnType = KSTypeRenderer.render(function.returnType)
        val delegateArguments = function.parameters.joinToString(", ", transform = ::renderDelegation)
        composeKDoc(function)?.let { appendKDocBlock(it) }
        function.sourceAnnotations?.let { appendLine("\t$it") }
        appendLine("\tpublic fun ${function.exportedName.backtick()}($parameters): $returnType =")
        appendLine("\t\t${function.qualifiedName.backtickLastSegment()}($delegateArguments)")
    }

    /**
     * Renders [rawKDoc] as a KDoc block indented under the surrounding `object`.
     * Each source line becomes a `* <line>` entry, with blank source lines preserved as blank
     * comment lines so Dokka sees paragraph boundaries. Any literal comment-close sequence in
     * [rawKDoc] is escaped so it can't terminate the generated block prematurely.
     */
    private fun StringBuilder.appendKDocBlock(rawKDoc: String) {
        appendLine("\t/**")
        rawKDoc.lines().forEach { line ->
            val trimmed = line.trimEnd().replace("*/", "*\\/")
            if (trimmed.isEmpty()) {
                appendLine("\t *")
            } else {
                appendLine("\t * $trimmed")
            }
        }
        appendLine("\t */")
    }

    /**
     * Assembles the KDoc that will sit above the wrapper: the source function's KDoc, with every
     * identifier rewritten to its exported form, followed by the `@param` tags lifted from each
     * `@Spread` parameter's data class (also rewritten). Returns `null` when neither source has
     * KDoc to contribute.
     */
    private fun composeKDoc(function: FunctionDescriptor): String? {
        val renames = collectRenames(function)
        val spreadOuterNames =
            function.parameters
                .filterIsInstance<ParameterDescriptor.Spread>()
                .map { it.originalName }
                .toSet()
        val functionKDoc =
            function.kdoc
                ?.let { KDocRewriter.stripParamTags(it, spreadOuterNames) }
                ?.let { KDocRewriter.rewrite(it, renames) }
        val spreadTags =
            function.parameters
                .filterIsInstance<ParameterDescriptor.Spread>()
                .mapNotNull { spread -> spread.dataClassKdoc?.let { KDocRewriter.extractParamTags(it) } }
                .flatten()
                .map { KDocRewriter.rewrite(it, renames) }

        if (functionKDoc.isNullOrBlank() && spreadTags.isEmpty()) return null
        return listOfNotNull(functionKDoc?.takeIf { it.isNotBlank() })
            .plus(spreadTags)
            .joinToString(separator = "\n")
    }

    /**
     * Builds the original-name -> exported-name map covering every parameter that appears in the
     * wrapper's signature - plain parameters and spread components alike - so [KDocRewriter] can
     * translate references uniformly across both the function KDoc and the spread class KDoc.
     */
    private fun collectRenames(function: FunctionDescriptor): Map<String, String> =
        function.parameters
            .flatMap { it.signatureContributions() }
            .associate { it.originalName to it.exportedName }

    /**
     * Wrapper parameters this descriptor puts on the generated function's signature:
     * plain parameters contribute themselves; spread parameters contribute their components,
     * so a data-class parameter turns into one wrapper parameter per constructor member.
     */
    private fun ParameterDescriptor.signatureContributions(): List<ParameterDescriptor.Plain> =
        when (this) {
            is ParameterDescriptor.Plain -> listOf(this)
            is ParameterDescriptor.Spread -> components
        }

    private fun renderPlain(parameter: ParameterDescriptor.Plain): String {
        val annotations = parameter.sourceAnnotations?.let { "$it " } ?: ""
        val signature = "$annotations${parameter.exportedName.backtick()}: ${KSTypeRenderer.render(parameter.type)}"
        return parameter.defaultExpression?.let { "$signature = $it" } ?: signature
    }

    /**
     * Delegation argument for one source-level parameter: `original = exported` for a plain
     * parameter, and `original = FQN(componentOriginal = componentExported, ...)` for a spread
     * one, so the source function receives a reconstructed data-class instance.
     */
    private fun renderDelegation(parameter: ParameterDescriptor): String =
        when (parameter) {
            is ParameterDescriptor.Plain -> {
                "${parameter.originalName.backtick()} = ${parameter.exportedName.backtick()}"
            }

            is ParameterDescriptor.Spread -> {
                val reconstruction =
                    parameter.components.joinToString(", ") {
                        "${it.originalName.backtick()} = ${it.exportedName.backtick()}"
                    }
                "${parameter.originalName.backtick()} = ${parameter.dataClassFqn.backtickLastSegment()}($reconstruction)"
            }
        }

    private companion object {
        const val QUARKDOWN_MODULE_FQN = "com.quarkdown.core.function.library.module.QuarkdownModule"
        const val MODULE_OF_FQN = "com.quarkdown.core.function.library.module.moduleOf"
    }
}
